package in.ashokit;

public class Employee {
	private int id;
	private String name;
	private int salaey;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalaey() {
		return salaey;
	}
	public void setSalaey(int salaey) {
		this.salaey = salaey;
	}
	

}
